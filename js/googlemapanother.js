$(function() {
  $('.s1').on('click',function(){
    if($('.s1').hasClass('off')){
        $(this).removeClass('off');
        $(this).animate({'marginRight':'90%'},500);
        $(this).addClass('on')

    }else{
      $(this).addClass('off');
      $(this).animate({'marginRight':'0'},500);
      $(this).removeClass('on');
    }
    });
});
